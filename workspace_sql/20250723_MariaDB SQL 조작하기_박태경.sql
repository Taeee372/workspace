CREATE TABLE BOOK_INFO(
	BOOK_NUM INT PRIMARY KEY, 
	TITLE VARCHAR(30) NOT NULL,
	WRITER VARCHAR(10) NOT NULL,
	PRICE INT,
	PUBLISHER VARCHAR(20) DEFAULT '미지정',
	PUB_DATE DATE
);

